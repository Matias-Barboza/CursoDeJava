package trabajosPracticos;

public class Ejercicio06Math {
    public static void main (String[] args){

        //Funciones trigonométricas habituales:

        System.out.println(Math.sin(23.56));
        System.out.println(Math.cos(45));
        System.out.println(Math.tan(90.5));
        System.out.println(Math.atan(50.5));
        System.out.println(Math.atan2(2.4,5.6));

        //La función exponencial y su inversa
        System.out.println(Math.exp(2));
        System.out.println(Math.log(5.6));

        //Las dos constantes PI y e
        System.out.println(Math.PI);
        System.out.println(Math.E);

    }
}
